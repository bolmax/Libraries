import UIKit

extension UIColor {
    
    open class var moeGreen: UIColor {
        get {
            return UIColor(red: 113.0 / 255.0, green: 162.0 / 255.0, blue: 64.0 / 255.0, alpha: 1)
        }
    }
    
    open class var moeCloudyBlue: UIColor {
        get {
            return UIColor(red: 180.0 / 255.0, green: 190.0 / 255.0, blue: 202.0 / 255.0, alpha: 1)
        }
    }
    
    open class var moeBluish: UIColor {
        get {
            return UIColor(red: 48.0 / 255.0, green: 152.0 / 255.0, blue: 207.0 / 255.0, alpha: 1)
        }
    }
    
    open class var moeRed: UIColor {
        get {
            return UIColor(red: 207.0 / 255.0, green: 48.0 / 255.0, blue: 48.0 / 255.0, alpha: 1.0)
        }
    }
    
    open class var moeShadow: UIColor {
        get {
            return UIColor(red: 6 / 255.0, green: 43 / 255.0, blue: 68 / 255.0, alpha: 1.0)
        }
    }
    
}
